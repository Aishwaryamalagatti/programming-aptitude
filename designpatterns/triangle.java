package com.xworkz.designpatterns;

public class triangle {

	public static void main(String[] args) {
	
				System.out.println("Print triangle");
				
				
				for(int i=1;i<=4;i++) {
					for(int j=1;j<=i;j++) 
					{
							System.out.print("*");
						}
					System.out.println(" ");
					}
					System.out.println();
			}
			}