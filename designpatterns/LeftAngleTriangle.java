package com.xworkz.designpatterns;

public class LeftAngleTriangle {

	public static void main(String[] args) {
		System.out.println("Print LeftAngleTriangle");
		int num = 4;
		for (int i = 0; i <= num; i++) {
			for (int j = 0; j <= 1; j++) {
				System.out.print("*");
			}
			System.out.println(" ");
		}
		System.out.println();

	}

}
